// Untuk menyisipkan baris baru, Anda dapat menggunakan \n karakter.

#include <stdio.h>

int main() {
    printf("halo semua! \n");
    printf("saya adalah manusia. \n");
    printf("saya mempunyai hobi yaitu. \n basket, \n musik, \n dan ngoding. \n");
    // Dua \n karakter yang berurutan akan menghasilkan baris kosong
    printf("Hello World! \n\n");
    // Membuat tab horizontal dengan (\t)
    printf("Hello World!\t");
    // Menyisipkan karakter garis miring terbalik (\) dengan (\\)
    printf("Hello World!\\");
    // Menyisipkan karakter tanda kutip ganda dengan (\")
    printf("They call him \"asep\".");
}